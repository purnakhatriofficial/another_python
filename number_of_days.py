number_of_days = int(input("Enter the number : "))


if number_of_days ==1:

    print(f"Sunday")

elif number_of_days == 2:

    print(f"Monday")
elif number_of_days ==3:
    
    print(f"Tuesday")

elif number_of_days == 4:

    print(f"Wednesday")

elif number_of_days == 5:

    print(f"Thrusday")

elif number_of_days == 6:

    print(f"Friday")

elif number_of_days == 7:

    print(f"Saturday")

else:

    print(f"Invalid Days")



